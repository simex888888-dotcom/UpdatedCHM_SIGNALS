# ── Ценовой спайк (OR-логика: достаточно одного из двух условий) ──────────────
# Было AND — требовало оба одновременно, сигнал приходил уже в момент пампа.
PRICE_SPIKE_1M      = 0.008       # >= 0.8% за 1 свечу  (было 1.2%)
PRICE_SPIKE_3M      = 0.012       # >= 1.2% за 3 свечи  (было 2.0%)

# ── Стакан ────────────────────────────────────────────────────────────────────
IMBALANCE_PUMP      = 0.62        # bid_vol / total > 62% → памп (было 0.68 — слишком редко)
IMBALANCE_DUMP      = 0.38        # bid_vol / total < 38% → дамп (было 0.32)
SPREAD_WIDENING_M   = 2.5         # спред > baseline * 2.5
