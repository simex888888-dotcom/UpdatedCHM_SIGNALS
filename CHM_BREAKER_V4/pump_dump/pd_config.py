OI_DUMP_PCT         = 0.025       # OI упал > 2.5% за 5 мин + цена вниз     (было 5%)
LONG_SHORT_PUMP     = 0.60        # L/S ratio < 0.60 → short squeeze (было 0.40 — крайне редко)
LONG_SHORT_DUMP     = 2.00        # L/S ratio > 2.00 → long squeeze  (было 3.50 — крайне редко)

